<?php

namespace GraphQL\SchemaObject;

class AkaConnectionQueryObject extends QueryObject
{
    const OBJECT_NAME = "AkaConnection";

    public function selectEdges(AkaConnectionEdgesArgumentsObject $argsObject = null)
    {
        $object = new AkaEdgeQueryObject("edges");
        if ($argsObject !== null) {
            $object->appendArguments($argsObject->toArray());
        }
        $this->selectField($object);

        return $object;
    }

    public function selectPageInfo(AkaConnectionPageInfoArgumentsObject $argsObject = null)
    {
        $object = new PageInfoQueryObject("pageInfo");
        if ($argsObject !== null) {
            $object->appendArguments($argsObject->toArray());
        }
        $this->selectField($object);

        return $object;
    }

    public function selectTotal()
    {
        $this->selectField("total");

        return $this;
    }
}
