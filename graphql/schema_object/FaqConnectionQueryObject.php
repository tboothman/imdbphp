<?php

namespace GraphQL\SchemaObject;

class FaqConnectionQueryObject extends QueryObject
{
    const OBJECT_NAME = "FaqConnection";

    public function selectEdges(FaqConnectionEdgesArgumentsObject $argsObject = null)
    {
        $object = new FaqEdgeQueryObject("edges");
        if ($argsObject !== null) {
            $object->appendArguments($argsObject->toArray());
        }
        $this->selectField($object);

        return $object;
    }

    public function selectPageInfo(FaqConnectionPageInfoArgumentsObject $argsObject = null)
    {
        $object = new PageInfoQueryObject("pageInfo");
        if ($argsObject !== null) {
            $object->appendArguments($argsObject->toArray());
        }
        $this->selectField($object);

        return $object;
    }

    public function selectTotal()
    {
        $this->selectField("total");

        return $this;
    }
}
