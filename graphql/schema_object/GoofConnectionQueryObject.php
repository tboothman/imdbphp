<?php

namespace GraphQL\SchemaObject;

class GoofConnectionQueryObject extends QueryObject
{
    const OBJECT_NAME = "GoofConnection";

    public function selectEdges(GoofConnectionEdgesArgumentsObject $argsObject = null)
    {
        $object = new GoofEdgeQueryObject("edges");
        if ($argsObject !== null) {
            $object->appendArguments($argsObject->toArray());
        }
        $this->selectField($object);

        return $object;
    }

    public function selectPageInfo(GoofConnectionPageInfoArgumentsObject $argsObject = null)
    {
        $object = new PageInfoQueryObject("pageInfo");
        if ($argsObject !== null) {
            $object->appendArguments($argsObject->toArray());
        }
        $this->selectField($object);

        return $object;
    }

    public function selectTotal()
    {
        $this->selectField("total");

        return $this;
    }
}
