<?php

namespace GraphQL\SchemaObject;

class PlotConnectionQueryObject extends QueryObject
{
    const OBJECT_NAME = "PlotConnection";

    public function selectEdges(PlotConnectionEdgesArgumentsObject $argsObject = null)
    {
        $object = new PlotEdgeQueryObject("edges");
        if ($argsObject !== null) {
            $object->appendArguments($argsObject->toArray());
        }
        $this->selectField($object);

        return $object;
    }

    public function selectPageInfo(PlotConnectionPageInfoArgumentsObject $argsObject = null)
    {
        $object = new PageInfoQueryObject("pageInfo");
        if ($argsObject !== null) {
            $object->appendArguments($argsObject->toArray());
        }
        $this->selectField($object);

        return $object;
    }

    public function selectRestriction(PlotConnectionRestrictionArgumentsObject $argsObject = null)
    {
        $object = new PlotRestrictionQueryObject("restriction");
        if ($argsObject !== null) {
            $object->appendArguments($argsObject->toArray());
        }
        $this->selectField($object);

        return $object;
    }

    public function selectTotal()
    {
        $this->selectField("total");

        return $this;
    }
}
