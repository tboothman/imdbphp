<?php

namespace GraphQL\SchemaObject;

class ReviewsConnectionQueryObject extends QueryObject
{
    const OBJECT_NAME = "ReviewsConnection";

    public function selectEdges(ReviewsConnectionEdgesArgumentsObject $argsObject = null)
    {
        $object = new ReviewEdgeQueryObject("edges");
        if ($argsObject !== null) {
            $object->appendArguments($argsObject->toArray());
        }
        $this->selectField($object);

        return $object;
    }

    public function selectPageInfo(ReviewsConnectionPageInfoArgumentsObject $argsObject = null)
    {
        $object = new PageInfoQueryObject("pageInfo");
        if ($argsObject !== null) {
            $object->appendArguments($argsObject->toArray());
        }
        $this->selectField($object);

        return $object;
    }

    public function selectTotal()
    {
        $this->selectField("total");

        return $this;
    }
}
