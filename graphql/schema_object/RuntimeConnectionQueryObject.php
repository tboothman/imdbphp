<?php

namespace GraphQL\SchemaObject;

class RuntimeConnectionQueryObject extends QueryObject
{
    const OBJECT_NAME = "RuntimeConnection";

    public function selectEdges(RuntimeConnectionEdgesArgumentsObject $argsObject = null)
    {
        $object = new RuntimeEdgeQueryObject("edges");
        if ($argsObject !== null) {
            $object->appendArguments($argsObject->toArray());
        }
        $this->selectField($object);

        return $object;
    }

    public function selectPageInfo(RuntimeConnectionPageInfoArgumentsObject $argsObject = null)
    {
        $object = new PageInfoQueryObject("pageInfo");
        if ($argsObject !== null) {
            $object->appendArguments($argsObject->toArray());
        }
        $this->selectField($object);

        return $object;
    }

    public function selectTotal()
    {
        $this->selectField("total");

        return $this;
    }
}
